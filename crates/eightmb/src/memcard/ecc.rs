/// Calculate parity data for 128B slice
pub fn calc_ecc(data: &[u8; 128]) -> [u8; 3] {
    let mut column_parity: u8 = 0x77;
    let mut line_parity_0: u8 = 0x7F;
    let mut line_parity_1: u8 = 0x7F;
    for (i, b) in data.iter().enumerate() {
        column_parity ^= COLUMN_PARITY_MASKS[*b as usize] as u8;
        if PARITY_TABLE[*b as usize] != 0 {
            line_parity_0 ^= !i as u8;
            line_parity_1 ^= i as u8;
        }
    }
    [column_parity, line_parity_0 & 0x7F, line_parity_1]
}

/// Calculate parity data for 512B page
pub fn calc_ecc_page(data: &[u8; 512]) -> [u8; 12] {
    let a = calc_ecc(&data[0..128].try_into().unwrap());
    let b = calc_ecc(&data[128..256].try_into().unwrap());
    let c = calc_ecc(&data[128..256].try_into().unwrap());
    let d = calc_ecc(&data[128..256].try_into().unwrap());

    [
        a[0], a[1], a[2], //
        b[0], b[1], b[2], //
        c[0], c[1], c[2], //
        d[0], d[1], d[2], //
    ]
}

